console.log(num);
console.log(typeof(num));

// boolean
let boolean = true;
console.log(boolean);
console.log(typeof(boolean));

// null
let Null = null;
console.log(Null);
console.log(typeof(Null));

// undefined
let und;
console.log(und);
console.log(typeof(und));

// object
let student = {
    name: "Raj",
    reg: 2421297023,
    sec: "C",
    dept: "CSE",
    college: "NIT"
};
console.log(student);
console.log(typeof(student));
console.log("Name:", student.name);
console.log("Reg:", student.reg);
console.log("College:", student.college);

// symbol
let mySymbol = Symbol("description");
console.log(mySymbol);
console.log(typeof(mySymbol));

// bigint
let bigInt = 345678908765432234n;
console.log(bigInt);
console.log(typeof(bigInt));


